#include <iostream>
using namespace std;
int main () {

int x = 9;
int y = 9;

cout << "x = " << x << endl;
cout << "y = " << y << endl << endl;
// increment dan decrement
// pre dan post

// increment
cout << x << endl;
cout << ++x << endl;
cout << x << endl;
cout << x++ << endl;
cout << x << endl << endl;

// decrement
cout << y << endl;
cout << --y << endl;
cout << y << endl;
cout << y-- << endl;
cout << y << endl;

return 0;
}